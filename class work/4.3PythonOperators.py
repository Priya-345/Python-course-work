#3. Assignment Operators
a = 10
print("Assignment Operators:",a) #Assignment Operators: 10
a+=4
print("Add & Assign Operators(a+=):",a) #Add & Assign Operators(a+=): 14 
a-=4
print("Subract & Assign(a-=):",a) #Subract & Assign(a-=): 10 
a*=4
print("Multiply & Divide(a*=):",a) #Multiply & Divide(a*=): 40
a/=4
print("Divide & Assign(a/=):",a) #Divide & Assign(a/=): 10.0       
a//=4
print("Floor Divide & Assign(a//=):",a) #Floor Divide & Assign(a//=): 2.0 
a%=4
print("Modulus & Assign(a%=):",a) #Modulus & Assign(a%=): 2.0 
a**=4
print("Exponentiate & Assign(a**=):",a) #Exponentiate & Assign(a**=): 16.0